// ?? Controllers
const reviewController = require('../controllers/reviewController')
const authController = require('../controllers/authController')

// ??????????????????? Node Modules ????????????????????????

// ??????????????????? Vendor Modules ??????????????????????
const express = require('express');

// ~~ Create Router for reviews and merged with tours
const router = express.Router({ mergeParams: true });

module.exports = router

// ^^^^^^^^^^^^^^^^^ Protected Routes ^^^^^^^^^^^^^^^^^^^^^

router.use(authController.protect)

// ~~ Root Route
router
    .route('/')
    .get(reviewController.getAllReviews)
    .post(authController.protect, authController.restrictTo('user'), reviewController.setTourUserId, reviewController.createReview)

// ~~ Review by ID Route
router
    .route('/:id')
    .get(reviewController.getReview)
    .patch(authController.restrictTo('user', 'admin'), authController.checkIfAuthor, reviewController.updateReview)
    .delete(authController.restrictTo('user', 'admin'), authController.checkIfAuthor, reviewController.deleteReview)